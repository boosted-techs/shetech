                        <h3 class="donation-details__title">Clean Water Projects in Action</h3>
<p class="donation-details__text">
Sympathy Africa working tirelessly to bring clean water to people of Mpigi District.
<br/><b>•	Water Wells:</b> Drilling wells in communities lacking access to clean water is a common approach. These wells provide a sustainable source of clean water for drinking, cooking, and sanitation.
<br/><b>•	Rainwater Harvesting:</b> Rainwater harvesting systems capture and store rainwater for domestic use. This approach is particularly valuable in regions with irregular access to groundwater.
<br/><b>•	Community Education:</b> Sympathy Africa Clean water projects also focus on educating communities about proper sanitation and hygiene practices. This knowledge is critical for preventing waterborne diseases.
<br/><b>•	Water Filtration Systems:</b> Installing water filtration systems helps purify water from local sources, making it safe for consumption. These systems are especially effective in areas with limited access to groundwater.

</p>

